function findFirstChar(str) {
  return str.charAt(0);
}
const str1 = `React`;
const str2 = `Elon Musk`;
const str3 = `Apple founder is Stew Job`;

let res1 = findFirstChar(str1);
console.log(res1);

let res2 = findFirstChar(str2);
console.log(res2);

let res3 = findFirstChar(str3);
console.log(res3);
